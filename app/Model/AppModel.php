class AppModel extends Model {
	/**
	* バリデーションルール
	*
	* 大小英字、半角数字のみ許可する
	*
	*/
	public function alphaNumeric($check) {
		$value = array_values($check);  // 配列の添字を数値添字に変換して・・・
		$value = $value[0];     // 最初の値を取る
		return preg_match('/^[a-zA-Z0-9]+$/', $value);
	}
}
